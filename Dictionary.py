# empty dictionary
my_dict = {}

# dictionary with integer keys
my_dict = {1: 'Ram', 2: 'Shyam'}
print(my_dict)
# dictionary with mixed keys
my_dict = {'name': 'Ram', 'no': [2, 4, 3]}
print(my_dict)
# using dict()
my_dict = dict({1:'IT', 2:'Web'})
print(my_dict)
# from sequence having each item as a pair
my_dict = dict([(1,'55'), (2,'78')])
print(my_dict)
